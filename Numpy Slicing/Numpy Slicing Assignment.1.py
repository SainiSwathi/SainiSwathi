#1.Write aa Numpy program to create an array of zeros,10 ones and 10 fives.

import numpy as np
zeros_array=np.zeros(10)
print("Array of zeros:")
print(zeros_array)

ones_array=np.ones(10)
print("Array of ones:")
print(ones_array)

fives_array=np.full(10,5)
print("Array of fives:")
print(fives_array)
